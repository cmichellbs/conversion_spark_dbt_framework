fgf.ID_GRUPO_FATURAMENTO as ID_GRUPO_FATURAMENTO,
1 as CH_DEFINICAO_ESTAGIO

FROM
"papakura_20221223"."dbo_mig"."fat_grupo_faturamento" fgf,REFERENCIAS