row_number() over(order by DT_MESANO_REF asc, fgf.ID_GRUPO_FATURAMENTO)as ID_CALENDARIO_FATURAMENTO,
REFERENCIAS.DT_MESANO_REF as DT_CALENDARIO_MESANO_REF,
